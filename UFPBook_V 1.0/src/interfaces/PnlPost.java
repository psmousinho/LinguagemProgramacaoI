    private void btnDelPostActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelPostActionPerformed
        mural.getDono().getMural().getPostagens().remove(post);
        mural.loadPostagens();
    }//GEN-LAST:event_btnDelPostActionPerformed

    public void loadComentarios() {
        pnlComents.removeAll();
        for(Comentario comentario : post.getComentarios()) {
            pnlComents.add(new PnlComentario(user,post, comentario, this));
        }
        pnlComents.revalidate();
    }
    
