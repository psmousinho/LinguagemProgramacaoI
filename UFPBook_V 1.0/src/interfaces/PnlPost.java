    private void btnComentarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComentarActionPerformed
            if(!txtFazerComent.getText().equals("")) {
                Comentario coment = new Comentario(user, txtFazerComent.getText());
                post.addComentario(coment);
                pnlComents.add(new PnlComentario(user,post, coment, this));
                pnlComents.revalidate();
            }
            
            scrollComents.setVisible(true);
            revalidate();
    }//GEN-LAST:event_btnComentarActionPerformed

