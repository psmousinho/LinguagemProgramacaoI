    public PnlPost(SubTelaMural mural, Postagem post, Usuario user) {
        initComponents();
        
        this.post = post;
        this.user = user;
        this.mural = mural;
        
        lblAutor.setText(post.getAutor().getNome());
        txtTexto.setText(post.getTexto());
        
        if(post.getImagem() != null) {
            ImageIcon imagem = new ImageIcon(post.getImagem().getPath());
            imagem.setImage(imagem.getImage().getScaledInstance(150, 130, Image.SCALE_SMOOTH));
            lblImagemPost.setIcon(imagem);
            lblImagemPost.addMouseListener(new MouseAdapter(){
                    public void mouseClicked(MouseEvent e){
                       JFrame frame = new JFrame();
                       ImageIcon img2 = new ImageIcon(post.getImagem().getPath());
                       img2.setImage(img2.getImage().getScaledInstance(800, 800, Image.SCALE_SMOOTH));
                       JLabel lbl = new JLabel(img2);
                       frame.add(lbl);
                       frame.setSize(800, 800);
                       frame.setLocationRelativeTo(null);
                       frame.setVisible(true);
                    }
                    public void mouseEntered(MouseEvent e) {
                        setCursor(Cursor.getPredefinedCursor(Cursor.HAND_CURSOR));
                    }

                    public void mouseExited(MouseEvent e) {
                        setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
                    }
                });
        }
        
        scrollComents.setVisible(false);
        
        if(user.equals(post.getAutor()) || mural.getMural().getAdmins().contains(user)) {
            btnDelPost.setVisible(true);
        } else {
            btnDelPost.setVisible(false);
        }
        
        loadComentarios();
    }

